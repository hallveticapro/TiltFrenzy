type SeedCard = Omit<Card, "id">;

function makeCards(deckId: string, cards: SeedCard[]): Card[] {
  return cards.map((card, index) => ({
    ...card,
    id: `${deckId}-${index + 1}`,
  }));
}

function word(
  prompt: string,
  category: string,
  difficulty: Difficulty = "medium",
): SeedCard {
  return { prompt, category, difficulty };
}

